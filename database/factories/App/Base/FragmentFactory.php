	namespace Database\Factories\App\Base;

	// App
	use Database\Factories\BaseFactory;
	use App\Models\App\Base\Fragment;
	use App\Models\App\Base\Item;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	CLASS CONSTRUCT
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class FragmentFactory extends BaseFactory
{

	protected $model = Fragment::class;


	public function definition(): array {

		// find or create an item as parent
		$item = Item::inRandomOrder()->first() ?? Item::factory()->create();

		return array_merge($this->getBaseProps(), [

			// fragment properties
			'parent_id' => 	$item->id,
			'parent_type' => Item::class,
			'template' => 	$this->faker->randomElement(['text', 'text-image', 'image', 'video', 'slider-image','accordion','separator','custom-component']),
		]);
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


}	// end class



