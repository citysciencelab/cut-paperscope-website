	namespace Database\Factories\Backend;

	// App
	use Database\Factories\BaseFactory;
	use App\Models\Backend\Setting;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	CLASS CONSTRUCT
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class SettingFactory extends BaseFactory
{

	protected $model = Setting::class;


	public function definition(): array {

		$category = $this->faker->randomElement(['general', 'seo', 'social', 'contact', 'other']);
		$identifier = strtolower($this->faker->word());

		return array_merge($this->getBaseProps(), [

			// setting properties
			'data_type' => 		$this->faker->randomElement(['number', 'string', 'bool', 'html', 'file']),
			'category' => 		$category,
			'identifier' => 	$identifier,
			'reference' => 		$category . '.' . $identifier,
			'content_de' => 	$this->faker->text(20)
		]);
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


}	// end class



