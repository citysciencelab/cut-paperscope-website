////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


return new class extends BaseMigration
{

	protected string $tableName = 'products';

	protected array $roles = [
		'editor' => 	['create', 'edit', 'delete'],
		'admin' => 		['create', 'edit', 'delete'],
	];



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    UP
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function create(Blueprint &$table): void {

		$this->setDefaultProps($table);
		$this->setPublishedProps($table);
		$this->setSharingProps($table);
		$this->setStripeProps($table);

		// product properties
		$table->string('title',50)->nullable();
		$table->text('teaser_description')->nullable();
		$table->string('teaser_image',256)->nullable();

		// paid properties
		$table->text('content')->nullable();

		$this->translate($table, ['title','teaser_description','teaser_image','content']);
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


}; // end class
