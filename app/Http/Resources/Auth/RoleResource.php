	namespace App\Http\Resources\Auth;

	// Laravel
	use Illuminate\Http\Resources\Json\JsonResource;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    CLASS CONSTRUCT
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class RoleResource extends JsonResource {


	public function toArray($request): array {

		return [

			'type' => 		'role',
			'id' => 		$this->id,
			'name' => 		$this->name,
			'guard_name' => $this->guard_name,
		];
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


} // end class
