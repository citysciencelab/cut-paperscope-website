	namespace App\Http\Resources;

	// Laravel
	use Illuminate\Http\Request;

	// App
	use App\Http\Resources\Base\BaseModelResource;
	use App\Http\Resources\Base\FragmentResource;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	CLASS
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class ProjectResource extends BaseModelResource {


	public function getProps(Request $request): array {

		return [

			// project properties
			'title' =>			$this->title,
			'description' =>	$this->translate('description'),

			// coordinates
			'start_longitude' =>	$this->start_longitude,
			'start_latitude' =>		$this->start_latitude,
			'end_longitude' =>		$this->end_longitude,
			'end_latitude' =>		$this->end_latitude,

			// scene
			'ratio' => 				$this->ratio,
			'scene' => 				$this->scene,
			'mapping' => 			$this->mapping,

			// timestamps
			'created_at' =>			$this->created_at,
			'updated_at' =>			$this->updated_at,
			'updated_timestamp' =>	$this->updated_at->timestamp,

			// relations
			'fragments' =>	$this->getChildRelation(FragmentResource::class,'fragments'),

			// backend properties
			//$this->addBackendProperties(['approved' => $this->approved]),
		];
	}



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


}
