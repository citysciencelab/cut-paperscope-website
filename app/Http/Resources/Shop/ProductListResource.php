	namespace App\Http\Resources\Shop;

	// Laravel
	use Illuminate\Http\Request;

	// App
	use App\Http\Resources\Base\BaseModelResource;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	CLASS
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class ProductListResource extends BaseModelResource {


	public function getProps(Request $request): array {

		return [

			// product properties
			'title' =>				$this->translate('title'),
			'teaser_description' =>	$this->translate('teaser_description'),
			'teaser_image' =>		$this->translate('teaser_image'),

			// stripe properties
			'stripe_id' =>			$this->stripe_id,
			'stripe_name' =>  		$this->stripe_name,
			'stripe_description' => $this->stripe_description,
			'stripe_price_value' => $this->stripe_price_value,
			'receipt' => 			$this->whenPivotLoaded('product_user', fn() => $this->pivot->receipt),
			'status' => 			$this->whenPivotLoaded('product_user', fn() => $this->pivot->status),

			// backend properties
			$this->addBackendProperties([
				'stripe_synced' =>  	$this->stripe_synced,
			]),
		];
	}



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


}
