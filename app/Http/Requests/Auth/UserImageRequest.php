	namespace App\Http\Requests\Auth;

	// Laravel
	use Illuminate\Support\Facades\Auth;

	// App
	use App\Http\Requests\BaseFormRequest;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	CLASS
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class UserImageRequest extends BaseFormRequest {


	public function authorize(): bool {

		$user = Auth::user();
		return $user && $user->hasPermissionTo('edit users');
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	VALIDATE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function rules(): array {

		return [

			'id' => 	 			'bail|required|uuid|exists:users',
			'stream_offset' => 		'bail|required|in:78fb153f02e9d3a43b4e5a81273ed716=', // hidden id for validation
		];
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


} // end class
