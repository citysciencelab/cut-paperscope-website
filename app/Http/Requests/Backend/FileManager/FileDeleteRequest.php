	namespace App\Http\Requests\Backend\FileManager;

	// Laravel
	use Illuminate\Support\Facades\Auth;

	// App
	use App\Http\Requests\BaseFormRequest;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	CLASS
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class FileDeleteRequest extends BaseFormRequest {


	public function authorize(): bool {

		$user = Auth::user();
		return $user && $user->hasPermissionTo('delete files');
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	VALIDATE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function rules(): array {

		return [

			'path' => 			'bail|required|string|max:256',
			'size' => 			'bail|required|numeric',
			'last_modified' => 	'bail|required|numeric',
			'storage' => 		$this->rule('storage'),
		];
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


} // end class
