	namespace App\Http\Requests\Backend\Base;

	// App
	use App\Http\Requests\Model\BaseModelSaveRequest;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// 	CLASS CONSTRUCT
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class SettingSaveRequest extends BaseModelSaveRequest
{

	protected $target = "settings";
	protected $targetClass = \App\Models\Backend\Setting::class;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	VALIDATE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function rules(): array {

		return $this->translate([

			...$this->getBaseRules(),

			// setting properties
			'data_type' => 		'bail|required|in:number,string,bool,html,file',
			'category' => 		'bail|required|string|max:50',
			'identifier' => 	'bail|required|string|max:50',
			'content' =>		'bail|nullable|translate',
		]);
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


} // end class
