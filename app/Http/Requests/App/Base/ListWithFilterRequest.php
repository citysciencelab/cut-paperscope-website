	namespace App\Http\Requests\App\Base;

	// App
	use App\Http\Requests\BaseFormRequest;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// 	CLASS CONSTRUCT
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class ListWithFilterRequest extends BaseFormRequest {



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	VALIDATE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function rules(): array {

		return [

			'types' => 			'bail|nullable|array',
			'types.*' => 		'bail|nullable|string|in:article,video,podcast,event,faq',

			'tags' => 			'bail|nullable|array',
			'tags.*' => 		'bail|nullable|string|max:100',

			'order' => 			'bail|nullable|string|in:newest,oldest',

			// backend filters
			'direction' => 				'bail|nullable|string|in:asc,desc',
			'direction_property' => 	'bail|nullable|string|max:20|required_with:direction',
		];
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


} // end class
