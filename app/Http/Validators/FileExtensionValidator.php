class FileExtensionValidator
{



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// 	VALIDATE
//
/////////////////////////////////////////////////////////a//////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function validate(string $attribute, mixed $value, array $parameters): bool {

		$value = explode('?', $value)[0];

		return in_array(pathinfo($value, PATHINFO_EXTENSION), $parameters);
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */

} // end class
