	namespace App\Actions\Fortify;

	// Laravel
	use \Illuminate\Validation\Rules\Password;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	TRAIT
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


trait PasswordValidationRules {


	protected function passwordRules(): array {

		return [
			'required', 'string',
			new Password(8),
			'unique:password_insecure,password',
			'confirmed'
		];
	}



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


}
