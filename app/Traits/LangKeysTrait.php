trait LangKeysTrait {


	protected function getLangKeys(): array {

		$isMultiLang = config('app.features.multi_lang');
		$langs = $isMultiLang ? config('app.available_locales') : [''];

		// add prefix "_" to each lang
		if($isMultiLang) { $langs = array_map(fn($i) => '_'.$i,$langs); }

		return $langs;
	}



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


}
