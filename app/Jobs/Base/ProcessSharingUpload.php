	namespace App\Jobs\Base;

	// App
	use App\Jobs\Base\BaseJob;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	CLASS DECLARATION
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class ProcessSharingUpload extends BaseJob
{



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	HANDLE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function handle(): void {

		parent::handle();

		foreach($this->langKeys as $lang) {

			// social image
			$this->convertJpeg('social_image'.$lang, 1440, 720, 'social-sharing'.$lang.'-twitter', false);
			$this->convertJpeg('social_image'.$lang, 1280, 720, 'social-sharing'.$lang, true);
		}

		$this->saveModel();
	}



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


}
