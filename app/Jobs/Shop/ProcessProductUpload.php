	namespace App\Jobs\Shop;

	// App
	use App\Jobs\Base\BaseJob;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	CLASS DECLARATION
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class ProcessProductUpload extends BaseJob
{



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	HANDLE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function handle(): void {

		parent::handle();

		foreach($this->langKeys as $lang) {

			$this->convertJpeg('teaser_image'.$lang, 200, 150, 'teaser-image-mobile'.$lang.'-lr', false, 20, 0);
			$this->convertJpeg('teaser_image'.$lang, 400, 300, 'teaser-image-mobile'.$lang.'-hr', false, 70);
			$this->convertJpeg('teaser_image'.$lang, 400, 300, 'teaser-image-desktop'.$lang.'-lr', false, 40, 0);
			$this->convertJpeg('teaser_image'.$lang, 800, 600, 'teaser-image-desktop'.$lang.'-hr', true, 70);
		}

		$this->saveModel();
	}



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


}
