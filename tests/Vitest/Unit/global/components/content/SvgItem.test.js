	// vue
	import { mount } from '@vue/test-utils';

	// test component
	import SvgItem from '@global/components/content/SvgItem.vue';



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	SVG ICON
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	test('renders svg icon', async () => {

		// act
		const wrapper = mount(SvgItem, {
			props: {
				icon: 'app/name-test-icon',
			},
		});

		// assert
		wrapper.get('.svg-icon');
		expect(wrapper.html()).toContain('sprite-icons.svg');
		expect(wrapper.html()).toContain('name-test-icon');
	});



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	SVG SPRITE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	test('renders svg sprite', async () => {

		// act
		const wrapper = mount(SvgItem, {
			props: {
				sprite: 'app/name-test-sprite',
			},
		});

		// assert
		wrapper.get('.svg-sprite');
		expect(wrapper.html()).toContain('sprite.svg');
		expect(wrapper.html()).toContain('name-test-sprite');
	});



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	EXTERNAL SVG
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	test('renders external svg', async () => {

		// act
		const wrapper = mount(SvgItem, {
			props: {
				url: 'https://www.hello-nasty.com/svg/app/sprite.svg',
			},

		});

		// assert
		wrapper.get('img.svg-sprite');
		expect(wrapper.html()).toContain('https://www.hello-nasty.com/svg/app/sprite.svg');
	});



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	INLINE SVG
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	test('renders inline', async () => {

		// act
		const wrapper = mount(SvgItem, {
			props: {
				inline: 'https://www.hello-nasty.com/svg/app/sprite.svg',
			},
			global: {
				stubs: {
					InlineSvg: {
						template: '<img class="svg-inline"/>',
					}
				},
			}
		});

		// assert
		wrapper.emitted('loaded');
		wrapper.get('.svg-inline');
	});



