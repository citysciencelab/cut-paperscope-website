describe('Edit Project model in backend', () => {



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	EDIT PROJECT
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	it('overwrite name', () => {

		// arrange: got to list page
		cy.backendLogin();
		cy.visit('/backend/project');
		cy.wait(2000);

		// arrange: find cypress test project
		var projectName;
		var project = cy.get('.data-list-item[data-draggable] a').filter(':contains("Cypress Test-Project")').first();
		project.should(target => projectName = target.text().trim().replace(' - edited',''));
		project.click();
		cy.wait(2000);

		// act
		cy.get('#input-name').then(el => cy.get(el).clear().type(projectName + ' - edited'));
		cy.get('.btn-model-confirm').click();

		// assert
		cy.url().should('include', '/backend/project');
		cy.get('.data-list-item[data-draggable] a').then(e => cy.get(e).filter(':contains("'+projectName+' - edited")').should('exist'));
	});



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


})
