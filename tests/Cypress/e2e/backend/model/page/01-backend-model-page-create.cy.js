describe('Create Page model in backend', () => {



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	CREATE PAGE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	it('create with correct data', () => {

		const random = Math.random().toString(36).substring(7);

		// arrange: got to edit page
		cy.backendLogin();
		cy.visit('/backend/page/edit');
		cy.wait(2000);

		// act
		cy.get('#input-name').type('Cypress Test-Page ' + random);
		cy.get('#input-published-start').click();
		cy.wait(1000); // popup opening
		cy.get('.date-calendar-day.today').first().click();
		cy.get('.date-selector-confirm').click();
		cy.wait(1000); // popup closing
		cy.get('#input-public-0').check({force:true});
		// page properties
		cy.get('#input-navi-visible-0').check({force:true});
		cy.get('#input-navi-label').type('Test-Label '+random);
		cy.get('#input-title').type('Cypress Test-Page Title');
		cy.get('.btn-model-confirm').click();

		// assert: page in list
		cy.url().should('include', '/backend/page');
		cy.get('.data-list-item[data-draggable] a').filter(':contains("Cypress Test-Page ' + random + '")').should('exist');
	});



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


})
