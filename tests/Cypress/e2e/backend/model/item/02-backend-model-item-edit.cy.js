describe('Edit Item model in backend', () => {



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	EDIT ITEM
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	it('overwrite name', () => {

		// arrange: got to list page
		cy.backendLogin();
		cy.visit('/backend/item');
		cy.wait(2000);

		// arrange: find cypress test item
		var itemName;
		var item = cy.get('.data-list-item[data-draggable] a').filter(':contains("Cypress Test-Item")').first();
		item.should(target => itemName = target.text().trim().replace(' - edited',''));
		item.click();
		cy.wait(2000);

		// act
		cy.get('#input-name').then(el => cy.get(el).clear().type(itemName + ' - edited'));
		cy.get('.btn-model-confirm').click();

		// assert
		cy.url().should('include', '/backend/item');
		cy.get('.data-list-item[data-draggable] a').then(e => cy.get(e).filter(':contains("'+itemName+' - edited")').should('exist'));
	});



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


})
