describe('Create Item model in backend', () => {



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	CREATE ITEM
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	it('create with correct data', () => {

		const random = Math.random().toString(36).substring(7);

		// arrange: got to edit page
		cy.backendLogin();
		cy.visit('/backend/item/edit');
		cy.wait(2000);

		// act
		cy.get('#input-name').type('Cypress Test-Item ' + random);
		cy.get('#input-published-start').click();
		cy.wait(1000); // popup opening
		cy.get('.date-calendar-day.today').first().click();
		cy.get('.date-selector-confirm').click();
		cy.wait(1000); // popup closing
		cy.get('#input-public-0').check({force:true});
		cy.get('#input-title').type('Cypress Test-Item Title');
		cy.get('.ck-editor__editable').then(el => {
			const editor = el[0].ckeditorInstance;
			editor.setData('Cypress Test-Item Description');
		})
		cy.get('.btn-model-confirm').click();

		// assert: item in list
		cy.url().should('include', '/backend/item');
		cy.get('.data-list-item[data-draggable] a').filter(':contains("Cypress Test-Item ' + random + '")').should('exist');
	});



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


})
