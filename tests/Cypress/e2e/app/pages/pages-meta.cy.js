/// <reference types="cypress" />
/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	TESTS
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


describe('Meta pages', () => {

	beforeEach(() => {
		cy.visit('/')
 	});



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	IMPRINT
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	it('access imprint page via index', () => {

		// assert: link in footer
		cy.get('.footer-navi-item').contains('Impressum').should('exist');
		cy.get('.footer-navi-item').contains('Impressum').click();

		// assert: access imprint page
		cy.wait(2000);
		cy.get('h1').contains('Impressum').should('exist');
	});



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	PR
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	it('access privacy page via index', () => {

		// assert: link in footer
		cy.get('.footer-navi-item').contains('Datenschutz').should('exist');
		cy.get('.footer-navi-item').contains('Datenschutz').click();

		// assert: access privacy page
		cy.wait(2000);
		cy.get('h1').contains('Datenschutz').should('exist');
	});



/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


})
