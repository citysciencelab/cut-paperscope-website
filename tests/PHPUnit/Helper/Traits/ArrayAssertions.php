trait ArrayAssertions {


	/**
	 * Assert that all values from the expected array are present in the actual array. Array values are not compared recursively.
	 *
	 * @param  array  $expected the values to check for
	 * @param  array  $actual   the array to check
	 */

	public function assertArraySubset($expected, $actual) {

		foreach($expected as $key => $value) {

			// skip array value. not recursive
			if(is_array($value)) { continue; }

			$this->assertEquals($actual[$key], $actual[$key]);
		}
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


}
