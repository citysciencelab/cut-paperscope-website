	namespace Tests\PHPUnit\Feature\Http\Controllers\App\Base;

	// Laravel
	use Tests\PHPUnit\TestCase;
	use Illuminate\Foundation\Testing\RefreshDatabase;

	// App
	use App\Models\App\Base\Item;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    CLASS DECLARATION
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class ItemControllerTest extends TestCase {

	use RefreshDatabase;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	MODEL LIST
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function test_get_public_list() {

		$this->seed();

		// act
		$data = $this->getData('/api/item');

		// assert: correct data
		$this->assertIsArray($data);
		$this->assertEquals('item',$data[0]['type']);
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	MODEL GET
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function test_get_public() {

		// arrange
		$item = Item::factory()->public()->create();

		// act
		$data = $this->getData('/api/item/'.$item->slug);

		// assert: check response is item model
		$this->assertEquals('item',$data['type']);
	}


	public function test_get_public_not_found() {

		$this->getError('/api/item/3ec9fc3b-a102-40b6-bd61-743f8bd2403b');
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


} // end class

