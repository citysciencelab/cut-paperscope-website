	namespace Tests\PHPUnit\Feature\Http\Controllers\Backend\Base;

	// Laravel
	use Tests\PHPUnit\TestCase;
	use Illuminate\Foundation\Testing\RefreshDatabase;

	// App
	use App\Models\App\Base\Page;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    CLASS DECLARATION
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class BackendPageControllerTest extends TestCase {

	use RefreshDatabase;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	MODEL LIST
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function test_get_model_list_in_backend() {

		// arrange
		$this->seed();
		$this->loginAsAdmin();

		// act
		$data = $this->postData('/api/backend/page', [], $this->getBackendHeaders());

		// assert
		$this->assertIsArray($data);
		$this->assertEquals('page',$data[0]['type']);
		$this->assertTrue(isset($data[0]['public']));
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	MODEL GET
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function test_get_model_in_backend() {

		// arrange
		$this->loginAsAdmin();
		$page = Page::factory()->create(['public'=>false]);

		// act
		$data = $this->getData('/api/backend/page/'.$page->id, [], $this->getBackendHeaders());

		// assert
		$this->assertEquals('page',$data['type']);
	}


	public function test_get_model_in_backend_not_found() {

		// arrange
		$this->loginAsAdmin();

		// act and assert
		$this->getError('/api/backend/page/3ec9fc3b-a102-40b6-bd61-743f8bd2403b');
	}


	public function test_get_model_in_backend_unauthenticated() {

		// arrange
		$page = Page::factory()->create(['public'=>false]);

		// act
		$response = $this->getJson('/api/backend/page/'.$page->id, $this->getBackendHeaders());

		// assert
		$response->assertStatus(401);
		$response->assertJson(['message'=>'Unauthenticated.']);
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


} // end class

