	namespace Tests\PHPUnit\Feature\Http\Requests\Backend\Base;

	// Laravel
	use Tests\PHPUnit\TestCase;
	use Illuminate\Foundation\Testing\RefreshDatabase;
	use Illuminate\Support\Facades\Bus;

	// App
	use App\Jobs\Base\ProcessItemUpload;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    CLASS DECLARATION
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class ItemSaveRequestTest extends TestCase {

	use RefreshDatabase;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    SAVE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function test_save_item() {

		// arrange
		Bus::fake();
		$this->loginAsEditor();

		// arrange: form data
		$formData = $this->getBaseFormData();
		$formData['title'] = 'test title';
		$formData['richtext'] = '<p>test content</p>';
		$formData['file'] = 'test.jpg';

		// act
		$item = $this->postData('/api/backend/item/save', $formData, $this->getBackendHeaders());

		// assert
		$this->assertEquals('item',$item['type']);
		$this->assertArraySubset($formData, $item);

		// assert: jobs
		Bus::assertDispatched(ProcessItemUpload::class);
	}


	private function getBaseFormData() {

		return [
			'name' => 'test name',
			'slug' => 'test-slug',
			'public' => true,
			'order' => 0,
			'published_start' => '1.2.2023 12:34',
		];
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */

} // end class

