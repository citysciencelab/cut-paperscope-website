	namespace Tests\PHPUnit\Feature\Http\Requests\Backend\Base;

	// Laravel
	use Tests\PHPUnit\TestCase;
	use Illuminate\Foundation\Testing\RefreshDatabase;
	use Illuminate\Support\Facades\Bus;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    CLASS DECLARATION
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class SettingSaveRequestTest extends TestCase {

	use RefreshDatabase;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    SAVE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function test_save_setting() {

		// arrange
		Bus::fake();
		$this->loginAsEditor();

		// arrange: form data
		$formData = $this->getBaseFormData();
		$formData['category'] = 'Global Settings';
		$formData['identifier'] = 'test identifier';
		$formData['data_type'] = 'string';
		$formData[$this->translateProp('content')] = 'test content';

		// act
		$setting = $this->postData('/api/backend/setting/save', $formData, $this->getBackendHeaders());

		// assert
		$this->assertEquals('setting',$setting['type']);
		$this->assertArraySubset($setting, $setting);
	}


	private function getBaseFormData() {

		return [
			'name' => 'test name',
			'public' => true,
			'order' => 0,
		];
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */

} // end class

