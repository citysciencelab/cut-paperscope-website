	namespace Tests\PHPUnit\Feature\Http\Requests\Backend\Shop;

	// Laravel
	use Tests\PHPUnit\TestCase;
	use Illuminate\Foundation\Testing\RefreshDatabase;
	use Illuminate\Support\Facades\Bus;

	// App
	use App\Jobs\Shop\SyncStripeProduct;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    CLASS DECLARATION
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class ProductSaveRequestTest extends TestCase {

	use RefreshDatabase;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    SAVE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function test_save_product() {

		// arrange
		Bus::fake();
		$this->loginAsEditor();

		// arrange: form data
		$formData = $this->getBaseFormData();
		$formData[$this->translateProp('title')] = 'test title';
		$formData[$this->translateProp('teaser_description')] = '<p>test description</p>';
		$formData[$this->translateProp('teaser_image')] = null;
		$formData[$this->translateProp('content')] = '<p>test content</p>';
		$formData['stripe_id'] = 'prod_1234';

		// act
		$product = $this->postData('/api/backend/product/save', $formData, $this->getBackendHeaders());

		// assert
		$this->assertEquals('product',$product['type']);
		$this->assertArraySubset($formData, $product);

		// assert: jobs
		Bus::assertDispatched(SyncStripeProduct::class);
	}


	private function getBaseFormData() {

		return [
			'name' => 'test name',
			'slug' => 'test-slug',
			'public' => true,
			'order' => 0,
			'published_start' => '1.2.2023 12:34',
		];
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */

} // end class

