	namespace Tests\PHPUnit\Feature\Notifications;

	// Laravel
	use Tests\PHPUnit\TestCase;
	use Illuminate\Foundation\Testing\RefreshDatabase;

	// App



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    CLASS DECLARATION
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


class SubscriptionResumedNotificationTest extends TestCase {

	use RefreshDatabase;



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	MAIL
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	public function test_notification_mail() {

		// arrange
		$user = $this->createUser();

		// act
		$notification = new \App\Notifications\SubscriptionResumedNotification('de');
		$mailMessage = $notification->toMail($user);

		// assert: notification driver
		$this->assertEquals(['mail'], $notification->via($user));

		// assert: mail message
		$this->assertEquals('Subscription wieder aktiviert', $mailMessage->subject);
		$this->assertEquals('Hallo '.$user->name, $mailMessage->greeting);
	}



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


} // end class

