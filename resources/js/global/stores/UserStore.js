	import { ref } from 'vue'
	import { defineStore } from 'pinia'



/*///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//	STORE
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// */


	export const useUserStore = defineStore('user', () => {

		const user = ref(window.config.app_user);

		function setUser(data) { user.value = data; }
		function setUserImage(newImage) { user.value.image = newImage }

		return {
			user,
			setUser, setUserImage,
		};
	})
