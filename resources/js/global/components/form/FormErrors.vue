<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    HTML
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<template>

		<p class="error form-errors" v-if="hasErrors">
			<template v-for="error in errors">
				{{ t( Array.isArray(error) ? error[0] : error) }}<br>
			</template>
		</p>

	</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<script setup>

		import { computed } from 'vue';


		/////////////////////////////////
		// INIT
		/////////////////////////////////

		const props = defineProps({

			errors: { type:Object, default: undefined }		// errors object from laravel validation
		});

		const hasErrors = computed(() => props.errors && Object.keys(props.errors).length > 0);


	</script>


