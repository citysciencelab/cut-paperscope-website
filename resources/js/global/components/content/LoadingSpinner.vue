//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<template>

		<svg-item class="loading-spinner" :style="css" :inline="webContext+'/'+icon"/>

	</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<script setup>

		import { ref, computed } from 'vue';


		/////////////////////////////////
		// INIT
		/////////////////////////////////

		const props = defineProps({
			size: 	{ type: Number, required:false }, 			// optionaly set the size of the spinner
			icon: 	{ type: String, default:'loading-api' }, 	// optionaly set the icon
		});

		const css = computed(() => {
			return !props.size ? null : { 'width':`${props.size}px`, 'height':`${props.size}px`};
		});


	</script>


