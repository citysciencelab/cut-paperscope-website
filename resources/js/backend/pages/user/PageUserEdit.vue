	<template>

		<user-edit class="content" v-if="user" :target="user"/>

	</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<script setup>

		import { ref } from 'vue';
		import { useRoute } from 'vue-router';
		import { usePage } from '@global/composables/usePage';
		import { useApi } from '@global/composables/useApi';


		/////////////////////////////////
		// INIT
		/////////////////////////////////

		const { t } = usePage("Backend");


		/////////////////////////////////
		// USER
		/////////////////////////////////

		const route = useRoute();
		const { apiPost } = useApi();
		const user = ref(null);

		if(route.params.id) {
			apiPost('backend.user',{id:route.params.id}, data => user.value = data);
		}
		else {
			user.value = {};
		}


	</script>


