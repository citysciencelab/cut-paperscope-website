	<script setup>

		import { ref } from 'vue';
		import { useRoute, useRouter } from 'vue-router';


		/////////////////////////////////
		// INIT
		/////////////////////////////////

		const route = useRoute();
		const router = useRouter();

		const props = defineProps({

		});

		const filters = ref({
			order: ['newest', 'oldest'].includes(route.query.order) ? route.query.order : 'newest',
		});

		const emit = defineEmits(['update']);


		/////////////////////////////////
		// ORDER
		/////////////////////////////////

		const itemsOrder = ref([
			{ 'Neueste': 'newest' },
			{ 'Ã„lteste': 'oldest' },
		]);


		/////////////////////////////////
		// UPDATE
		/////////////////////////////////

		function onUpdate() {

			router.replace({ query: {...route.query, ...filters.value} });
			emit('update', filters.value);
		}

		onUpdate();


	</script>


