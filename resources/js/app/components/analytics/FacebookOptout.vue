	<template>

		<div class="facebook-optout form-row">
			<btn class="secondary small" @click="toggle" :label="t(hasFacebook?'Facebook-Pixel aktivieren':'Facebook-Pixel deaktivieren')"/>
		</div>

	</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<script setup>

		import { ref } from 'vue';


		/////////////////////////////////
		// INIT
		/////////////////////////////////

		const hasFacebook = ref(!!Cookies.get('piwik_ignore'));

		function toggle() {

			if (hasFacebook.value) { localStorage.removeItem('opt-out') }
			else { localStorage.setItem('opt-out', 'true'); }

			hasFacebook.value = !hasFacebook.value;
		}

	</script>



