	<template>

		<div class="matomo-optout form-row">
			<btn class="secondary small" @click="toggle" :label="t(hasMatomo?'Matomo aktivieren':'Matomo deaktivieren')"/>
		</div>

	</template>



<!--/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    JAVASCRIPT
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////-->


	<script setup>

		import { ref } from 'vue';
		import Cookies from 'js-cookie';


		/////////////////////////////////
		// INIT
		/////////////////////////////////

		const hasMatomo = ref(!!Cookies.get('piwik_ignore'));

		function toggle() {

			if (hasMatomo.value) { Cookies.remove('piwik_ignore'); }
			else { Cookies.set('piwik_ignore', '1', { expires: 365 }); }

			hasMatomo.value = !hasMatomo.value;
		}

	</script>



